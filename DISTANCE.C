#include <exec\types.h>

/*----------------------------------------------------------------------*/
/* returns sqroot(dx*dx + dy*dy);   					*/
/*----------------------------------------------------------------------*/
SHORT Distance(dx,dy) SHORT dx,dy; {
    SHORT v,t,i;
    long n;
    n = (long)dx*(long)dx + (long)dy*(long)dy;
    i = 1024; 
    v = 0;
    while (i!=0) { t = v+i; if ( ((long)t*(long)t) <= n ) v = t; i >>= 1; }
    return(v);
    }

